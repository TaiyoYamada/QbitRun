import SwiftUI
import UIKit

enum BlochAxisPalette {
    static let zAxisUIColor = UIColor(red: 0.2, green: 0.2, blue: 1.0, alpha: 1.0)
    static let xAxisUIColor = UIColor.cyan
    static let yAxisUIColor = UIColor.purple

    static let zAxisColor = Color(uiColor: zAxisUIColor)
    static let xAxisColor = Color(uiColor: xAxisUIColor)
    static let yAxisColor = Color(uiColor: yAxisUIColor)
}
